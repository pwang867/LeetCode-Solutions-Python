# other methods: iteration, BFS, DFS


# recursion
class Solution(object):
    def invertTree(self, root):
        """
        :type root: TreeNode
        :rtype: TreeNode
        """
        if not root:
            return None
        
        if root and not root.left and not root.right:
            return root
        
        root.left, root.right = self.invertTree(root.right), self.invertTree(root.left)
        
        return root



