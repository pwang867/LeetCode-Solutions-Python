# bit manipulation, O(log(n)-log(m))
class Solution(object):
    def rangeBitwiseAnd(self, m, n):
        """
        :type m: int
        :type n: int
        :rtype: int
        """
        while n > m:
            n = n&(n-1)   # remove the last set bit
        return n


